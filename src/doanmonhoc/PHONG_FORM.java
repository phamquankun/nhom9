    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rIndex = jTable1.getSelectedRow();
        sophong1.setText(model.getValueAt(rIndex, 0).toString());
        jComboBox1.setSelectedItem(model.getValueAt(rIndex, 1));
        String isdadat = model.getValueAt(rIndex, 2).toString();
        if (isdadat.equals("Đã Đặt")) {
            jRadioButton1.setSelected(true);
        } else if (isdadat.equals("Chưa Đặt")) {
            jRadioButton2.setSelected(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

