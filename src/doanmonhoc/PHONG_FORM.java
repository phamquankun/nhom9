    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int ret = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm phòng?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (ret == JOptionPane.YES_OPTION) {
            try {
                int sophong = Integer.valueOf(sophong1.getText());
                String loaiphong = String.valueOf(jComboBox1.getSelectedItem().toString());
                if (ph.themphong(sophong, loaiphong)) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm phòng thành công!", "Them phong", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Thêm phòng không thành công!", "Enror", JOptionPane.WARNING_MESSAGE);
                }

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Nhập vào số phòng (number)", "Room ID enror", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

