    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int sophong = 0;
        String loaiphong = String.valueOf(jComboBox1.getSelectedItem().toString());

        String tinhtrang = "Chưa Đặt";
        if (jRadioButton1.isSelected()) {
            tinhtrang = "Đã Đặt";
        }
        int ret = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa phòng?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (ret == JOptionPane.YES_OPTION) {
            try {
                sophong = Integer.valueOf(sophong1.getText());
                if (ph.suaphong(sophong, loaiphong, tinhtrang)) {
                    JOptionPane.showMessageDialog(rootPane, "Sửa dữ liệu thành công!", "Sửa phòng", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Sửa dữ liệu không thành công!", "Enror", JOptionPane.WARNING_MESSAGE);
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Nhập vào số phòng (number)", "Room ID enror", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

